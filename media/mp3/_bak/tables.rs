/// MPEG-1 Layer 3 requantization: x^(4/3) approximation table (precomputed).
/// pow_table[i] = i^(4/3) for i in 0..8207
pub fn pow43(x: i32) -> f64 {
    let fx = x.abs() as f64;
    let result = fx.powf(4.0 / 3.0);
    if x < 0 { -result } else { result }
}

/// Dequantization scale: 2^(-0.25 * (global_gain - 210))
pub fn dequant_scale(global_gain: u8) -> f64 {
    2.0_f64.powf(-0.25 * (global_gain as f64 - 210.0))
}

/// Scalefactor multiplier: 2^(-0.5 * scalefac_multiplier * scalefac)
pub fn scalefac_scale(scalefac: i32, multiplier: bool) -> f64 {
    let m = if multiplier { 1.0 } else { 0.5 };
    2.0_f64.powf(-m * scalefac as f64)
}
